function operacoes(a, b) {
    let sum = a + b;
    console.log("Soma = " + sum);

    let sub = a - b;
    console.log("Subtracão = " + sub);

    let mult = a * b;
    console.log("Multiplicação = " + mult);

    let div = a / b;
    console.log("Divisão = " + div);

    let mod = a % b;
    console.log("Módulo = " + mod);

    return true;
};
